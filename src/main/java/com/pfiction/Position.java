package com.pfiction;

public class Position {
    private int x;
    private int y;

    // Definir posição e criar Getters e Setters para ajudar a chamar informações com maior facilidade

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
